    private void loadTable(List<NhanVien> ds)
    {
        DefaultTableModel dtm = (DefaultTableModel) this.tblNV.getModel();
        dtm.setRowCount(0);
        for (NhanVien nv : ds) {
            Object[] row = {
                nv.getId(),
                nv.getTen(),
                nv.getMa(),
                nv.getTenDangNhap(),
                nv.getTrangThai(),
            };
            dtm.addRow(row);
        }
    }
    
